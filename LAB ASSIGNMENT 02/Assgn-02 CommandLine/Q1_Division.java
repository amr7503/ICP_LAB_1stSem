class Q1_Division
{
  public static void main(String args[])
  {
   int divd=Integer.parseInt(args[0]);
   int divs=Integer.parseInt(args[1]);
   
   System.out.println("Dividend = "+divd);
   System.out.println("Divisor = "+divs);
   
   System.out.println("Quotient = "+(divd/divs));
   System.out.println("Remainder = "+(divd%divs));
   }
 }
